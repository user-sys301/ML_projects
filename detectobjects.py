    if original_img is None:
        return

    # 1. Normalize size
    img = cv2.resize(original_img, (800, 800))
    output_img = img.copy()

    # 2. Filtering
    gaussian_blur = cv2.GaussianBlur(img, (5, 5), 0)
    filtered = cv2.bilateralFilter(gaussian_blur, 5, 50, 50)

    # 3. Edge Detection
    gray = cv2.cvtColor(filtered, cv2.COLOR_BGR2GRAY)
    edges = cv2.Canny(gray, 30, 90)

    # 4. Light Morphological Closing (Reduced to 3x3 to stop items from merging)
    kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (3, 3))
    closed = cv2.morphologyEx(edges, cv2.MORPH_CLOSE, kernel, iterations=1)

    # 5. Use RETR_LIST instead of EXTERNAL to catch individual items inside merged boundaries
    contours, _ = cv2.findContours(closed, cv2.RETR_LIST, cv2.CHAIN_APPROX_SIMPLE)

    # Sort by area descending so we process the largest valid individual boundaries first
    contours = sorted(contours, key=cv2.contourArea, reverse=True)

    found_centers = []

    for c in contours:
        area = cv2.contourArea(c)

        # 6. Strict Area Filter to weed out merged clusters (>150k) and dust (<15k)
        if area < 15000 or area > 150000:
            continue

        x, y, w, h = cv2.boundingRect(c)
        aspect_ratio = float(w) / h
        if aspect_ratio < 0.6 or aspect_ratio > 1.4:
            continue

        # 7. Prevent duplicate contour lines around the same object
        M = cv2.moments(c)
        if M["m00"] == 0: continue
        cx = int(M["m10"] / M["m00"])
        cy = int(M["m01"] / M["m00"])

        is_duplicate = False
        for (fcx, fcy) in found_centers:
            # If a new contour's center is within 50px of an old one, ignore it
            if np.sqrt((cx - fcx)**2 + (cy - fcy)**2) < 50:
                is_duplicate = True
                break
        if is_duplicate:
            continue

        found_centers.append((cx, cy))

        # 8. Shape Math (Circularity)
        peri = cv2.arcLength(c, True)
        if peri == 0: continue

        circularity = 4 * np.pi * (area / (peri * peri))
        is_circle = circularity > 0.82

        # 9. Extract Object Mask for Color Analysis
        mask = np.zeros(gray.shape, dtype=np.uint8)
        cv2.drawContours(mask, [c], -1, 255, -1)

        obj_cropped = filtered[y:y+h, x:x+w]
        mask_cropped = mask[y:y+h, x:x+w]

        hsv_roi = cv2.cvtColor(obj_cropped, cv2.COLOR_BGR2HSV)

        # Calculate MEAN Brightness to completely bypass the glare issue
        v_channel = hsv_roi[:,:,2]
        mean_brightness = cv2.mean(v_channel, mask=mask_cropped)[0]

        # Check for specific pattern colors (Green & Purple)
        lower_green = np.array([35, 30, 40])
        upper_green = np.array([90, 255, 255])

        lower_purple = np.array([110, 20, 40])
        upper_purple = np.array([165, 255, 255])

        green_mask = cv2.inRange(hsv_roi, lower_green, upper_green)
        purple_mask = cv2.inRange(hsv_roi, lower_purple, upper_purple)

