def process_and_classify_objects(image_path):
    # 1. Read the image
    img = cv2.imread(image_path)
    if img is None:
        print(f"Could not read image: {image_path}")
        return

    # 2. Normalize size (Resizing to a fixed standard 800x800 for consistent computing)
    img = cv2.resize(img, (800, 800))
    output_img = img.copy()

    # 3. Remove blur without affecting edges (Bilateral Filter)
    # d=9, sigmaColor=75, sigmaSpace=75 are standard optimized values
    filtered = cv2.bilateralFilter(img, 9, 75, 75)

    # 4. Convert to Grayscale and apply Canny Edge Detection
    gray = cv2.cvtColor(filtered, cv2.COLOR_BGR2GRAY)
    edges = cv2.Canny(gray, 40, 120)

    # 5. Morphological closing to connect fragmented edges of the objects
    kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (7, 7))
    closed = cv2.morphologyEx(edges, cv2.MORPH_CLOSE, kernel, iterations=3)

    # 6. Find Contours
    contours, _ = cv2.findContours(closed, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    for c in contours:
        area = cv2.contourArea(c)

        # Filter out very small noise and the massive brown box itself
        if area < 8000 or area > 400000:
            continue

        # 7. Determine Shape (Square vs Circle)
        peri = cv2.arcLength(c, True)
        approx = cv2.approxPolyDP(c, 0.04 * peri, True)

        # A square usually has 4 corners. Allow 4-5 to account for slightly rounded edges
        is_square = len(approx) >= 4 and len(approx) <= 5

        # Circularity check as a fallback (4 * pi * area / perimeter^2)
        circularity = 4 * np.pi * (area / (peri * peri))
        if circularity > 0.8:
            is_square = False # High circularity means it's a circle

        # 8. Extract Region of Interest (ROI) for Color Classification
        x, y, w, h = cv2.boundingRect(c)
        roi = filtered[y:y+h, x:x+w]

        # Convert ROI to HSV for robust color detection under varying lighting
        hsv_roi = cv2.cvtColor(roi, cv2.COLOR_BGR2HSV)

        # Define HSV color boundaries for the patterns
        lower_green = np.array([35, 40, 40])
        upper_green = np.array([85, 255, 255])

        lower_purple = np.array([115, 30, 40])
        upper_purple = np.array([160, 255, 255])

        lower_black = np.array([0, 0, 0])
        upper_black = np.array([179, 255, 70]) # Low Value (V) indicates black

        # Create color masks
        green_mask = cv2.inRange(hsv_roi, lower_green, upper_green)
        purple_mask = cv2.inRange(hsv_roi, lower_purple, upper_purple)
        black_mask = cv2.inRange(hsv_roi, lower_black, upper_black)

        total_pixels = roi.shape[0] * roi.shape[1]

        # Calculate percentage of specific colors in the object
        green_ratio = cv2.countNonZero(green_mask) / total_pixels
        purple_ratio = cv2.countNonZero(purple_mask) / total_pixels
        black_ratio = cv2.countNonZero(black_mask) / total_pixels

        # 9. Classify based on Shape + Color Pattern Ratios
        label = "Unknown"
        color = (0, 255, 255) # Default yellow text

        if is_square:
            if black_ratio > 0.4:
                label = "Obj 1: Black Square"
                color = (0, 0, 0)
            elif green_ratio > 0.02: # Even 2% green pixels means it's the green pattern
                label = "Obj 2: White & Green Square"
                color = (0, 200, 0)
            elif purple_ratio > 0.02: # Even 2% purple pixels means it's the purple pattern
                label = "Obj 3: Purple & White Square"
                color = (200, 0, 200)
            else:
                label = "Square (Unclassified)"
        else: # It is circular
            if black_ratio > 0.4:
                label = "Obj 4: Black Circular"
                color = (0, 0, 0)
            else:
                # If it's a circle and not black, it must be the white one
                label = "Obj 5: White Circular"
                color = (255, 255, 255)

        # 10. Draw Results on Image
        cv2.drawContours(output_img, [c], -1, (0, 255, 0), 3) # Draw green contour

        # Put text background for better visibility
        (text_w, text_h), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.7, 2)
        cv2.rectangle(output_img, (x, y - text_h - 10), (x + text_w, y), (200, 200, 200), -1)

        # Put Text
        cv2.putText(output_img, label, (x, y - 5), cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2)

    # Display Output in Colab
    print(f"--- Results for {os.path.basename(image_path)} ---")
    cv2_imshow(output_img)
    print("\n")

