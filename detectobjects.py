    gaussian_blur = cv2.GaussianBlur(img, (0, 0), 3.0)
    sharpened = cv2.addWeighted(img, 1.5, gaussian_blur, -0.5, 0)

    # 4. Light Bilateral Filter to remove noise but keep the newly sharpened edges
    filtered = cv2.bilateralFilter(sharpened, 5, 50, 50)

    # 5. Grayscale & Canny Edge Detection (Lowered thresholds to catch fine gaps)
    gray = cv2.cvtColor(filtered, cv2.COLOR_BGR2GRAY)
    edges = cv2.Canny(gray, 30, 100)

    # 6. Dilation - Much smaller kernel (3x3) to connect lines without merging boxes!
    kernel = np.ones((3, 3), np.uint8)
    closed = cv2.dilate(edges, kernel, iterations=1)

    # 7. Find Contours
    contours, _ = cv2.findContours(closed, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    for c in contours:
        area = cv2.contourArea(c)

        # Strict area filtering (Ignoring dust and the brown box)
        if area < 12000 or area > 350000:
            continue

        # Get Aspect Ratio to filter out weird merged blobs (Squares/Circles have a ~1:1 ratio)
        x, y, w, h = cv2.boundingRect(c)
        aspect_ratio = float(w) / h
        if aspect_ratio < 0.7 or aspect_ratio > 1.3:
            continue

